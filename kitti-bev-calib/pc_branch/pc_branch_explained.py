"""
点云分支 (Point Cloud Branch) 详细注释版本
=============================================

输入配置（基于您的设置）:
- 点云: 原始LiDAR点 (B, 3, N)，坐标为 (x, y, z)
- BEV范围: X[0, 200m], Y[-100, 100m], Z[-10, 10m]
- 体素尺寸: X=0.25m, Y=0.25m, Z≈0.488m
- 稀疏网格: sparse_shape = (800, 800, 41) → (X, Y, Z)方向
- 每个体素最多10个点，总体素上限120,000

核心思想：
1. 将无序点云"体素化(Voxelize)"为规则3D网格
2. 用稀疏3D卷积(Sparse Conv3d)逐步提取特征并下采样
3. 压缩Z维度，生成2D BEV特征图
4. 通过ProjectionHead映射到统一维度(128)

可视化工作流：

    原始点云 (B, 3, N)
         ↓
    [体素化 PointToVoxel]
         ↓
    稀疏体素 (M, 3) + 坐标 (M, 4)
         ↓
    [稀疏3D卷积网络 SparseEncoder]
         ↓                                  空间尺寸变化:
    Layer 0: 残差块×2 + 下采样(stride=2)    (800,800,41) → (400,400,21)
    Layer 1: 残差块×2 + 下采样(stride=2)    (400,400,21) → (200,200,11)
    Layer 2: 残差块×2 + 下采样(stride=2)    (200,200,11) → (100,100,5)
    Layer 3: 残差块×2 (无下采样)            (100,100,5)  → (100,100,5)
    conv_out: Z方向压缩(stride_z=2)        (100,100,5)  → (100,100,2)
         ↓
    dense转换: (B, 100, 100, 2, 128)
         ↓
    Z×C展平: (B, 256, 100, 100)
         ↓
    [ProjectionHead] 256 → 128
         ↓
    BEV特征图 (B, 128, 100, 100)

与图像分支的对比:
┌──────────────┬─────────────────────┬─────────────────────┐
│              │ 图像分支 (Cam2BEV)  │ 点云分支 (Lidar2BEV)│
├──────────────┼─────────────────────┼─────────────────────┤
│ 输入         │ RGB图像 (3,360,640) │ 点云 (3, N)         │
│ 到BEV的方式  │ LSS (深度预测+投影) │ 体素化+稀疏卷积     │
│ 3D处理       │ bev_pool (CUDA)     │ SparseConv3d        │
│ Z层数(输出前)│ 5 (zbound步长=4m)   │ 2 (卷积压缩至2层)   │
│ 展平后维度   │ 128×5 = 640         │ 128×2 = 256         │
│ ProjectionHead│ 640 → 128          │ 256 → 128           │
│ 最终输出     │ (B, 128, 100, 100)  │ (B, 128, 100, 100)  │
│ Z信息保留    │ 受zbound步长影响    │ 始终由卷积决定      │
└──────────────┴─────────────────────┴─────────────────────┘
"""

import torch
import torch.nn as nn
from spconv.pytorch.utils import PointToVoxel
from .pc_encoders import SparseEncoder

import sys
import os
sys.path.append(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
from proj_head import ProjectionHead
from bev_settings import xbound, ybound, zbound, down_ratio, sparse_shape, vsize_xyz


class Lidar2BEV(nn.Module):
    """
    LiDAR点云 → BEV特征图 转换模块

    流程: 点云 → 体素化 → 稀疏3D卷积 → 压缩Z维 → 投影头 → BEV特征

    配置示例:
        xbound = (0, 200, 2.0)     → 体素化X范围
        ybound = (-100, 100, 2.0)  → 体素化Y范围
        zbound = (-10, 10, 4.0)    → 体素化Z范围
        sparse_shape = (800, 800, 41)  → 体素网格分辨率
        vsize_xyz = [0.25, 0.25, 0.488] → 每个体素的物理尺寸(米)
    """

    def __init__(self):
        super(Lidar2BEV, self).__init__()

        # ========================================================================
        # 1. 体素化器 (PointToVoxel) - 将无序点云转换为规则3D网格
        # ========================================================================
        #
        # 原理: 将3D空间划分为均匀的小立方体(体素)，
        #       每个体素收集落入其中的点云，类似3D版本的"直方图"
        #
        # 可视化:
        #     原始点云（无序）         体素网格（规则）
        #       • •  •                ┌─┬─┬─┬─┐
        #      •    •                 │•│ │•│ │  ← 每个格子最多10个点
        #     •  • •  •               ├─┼─┼─┼─┤
        #       • •                   │ │•│ │•│
        #                             └─┴─┴─┴─┘
        #     N个无序点      →      M个非空体素 (M << 总格子数)
        #
        self.ptvoxel = PointToVoxel(
            vsize_xyz = vsize_xyz,
            # 体素尺寸: [0.25m, 0.25m, 0.488m]
            # X方向: 200m / 800格 = 0.25m/格
            # Y方向: 200m / 800格 = 0.25m/格
            # Z方向: 20m / 41格 ≈ 0.488m/格
            #
            # 与图像分支BEV网格(2m/格)的区别:
            #   体素化分辨率(0.25m) 远高于 BEV输出分辨率(2m)
            #   高分辨率输入 → 卷积下采样8倍 → 最终BEV分辨率2m

            coors_range_xyz=(xbound[0], ybound[0], zbound[0], xbound[1], ybound[1], zbound[1]),
            # 空间范围: (0, -100, -10, 200, 100, 10)
            # X: [0, 200]m   → 前方200米
            # Y: [-100, 100]m → 左右各100米
            # Z: [-10, 10]m  → 上下各10米
            #
            # 超出此范围的点会被自动丢弃

            num_point_features=3,
            # 每个点3个特征: (x, y, z) 坐标本身作为特征
            # 也可以扩展为4(+强度)或更多

            max_num_voxels=120000,
            # 最多保留120,000个非空体素
            # 总格数: 800 × 800 × 41 = 26,240,000
            # 实际非空: ~2-5% ≈ 50万-130万
            # 如果超过12万，按体素内点数排序保留最重要的
            #
            # 120,000 << 26,240,000 → 极度稀疏(~0.46%)
            # 这就是为什么要用稀疏卷积而非普通卷积

            max_num_points_per_voxel=10,
            # 每个体素最多保留10个点
            # 0.25m × 0.25m × 0.488m 的小空间内通常不会超过10个LiDAR点

            device=torch.device('cuda' if torch.cuda.is_available() else 'cpu')
        )

        # ========================================================================
        # 2. 稀疏3D卷积编码器 - 从体素提取多尺度特征
        # ========================================================================
        #
        # SparseEncoder 结构:
        #   conv_input: 3→16通道 (SubMConv3d)
        #   Layer 0: [残差16, 残差16, 下采样16→32]  空间: ÷2
        #   Layer 1: [残差32, 残差32, 下采样32→64]  空间: ÷2
        #   Layer 2: [残差64, 残差64, 下采样64→128] 空间: ÷2
        #   Layer 3: [残差128, 残差128]             空间: 不变
        #   conv_out: 128→128, Z方向stride=2        Z: ÷2
        #
        # 空间尺寸变化 (X, Y, Z):
        #   输入:   (800, 800, 41)  → ~0.25m分辨率
        #   Layer0: (400, 400, 21)  → ~0.5m分辨率
        #   Layer1: (200, 200, 11)  → ~1m分辨率
        #   Layer2: (100, 100, 5)   → ~2m分辨率 ← 此时匹配BEV输出分辨率
        #   Layer3: (100, 100, 5)   → ~2m分辨率 (空间不变)
        #   conv_out:(100, 100, 2)  → Z维进一步压缩
        #
        self.sparse_encoder = SparseEncoder(sparse_shape=sparse_shape)
        # sparse_shape = (800, 800, 41)

        # ========================================================================
        # 3. 投影头 - 将多Z层特征压缩到固定维度
        # ========================================================================
        #
        # SparseEncoder输出: (B, 100, 100, Z=2, C=128)
        # Z×C展平后: (B, 2×128, 100, 100) = (B, 256, 100, 100)
        #
        # ProjectionHead: 256 → 128
        #   结构: Linear(256,128) → GELU → Linear(128,128) → Dropout
        #         + 残差连接 + LayerNorm
        #
        # 对比图像分支:
        #   图像分支: Z=5层, 128×5=640 → 128 (压缩比5:1)
        #   点云分支: Z=2层, 128×2=256 → 128 (压缩比2:1)
        #   点云分支压缩比更小 → 信息损失更少
        #
        self.proj_head = ProjectionHead()
        # 默认 embedding_dim=256, projection_dim=128

        self.voxelize_reduce = True
        # True: 体素内多个点的特征取SUM (而非保留所有点)
        # 这大幅减少了数据量: M×10×3 → M×3

        self.out_channels = self.proj_head.projection_dim
        # out_channels = 128
        # 与图像分支输出维度一致，便于后续融合

    @torch.no_grad()
    def voxelize(self, pc):
        """
        体素化 - 将点云分配到3D网格

        输入:
            pc: (B, N, 3) - B个点云，每个N个点，每点3个坐标

        输出:
            vox: (M_total, 3)         - 所有batch的非空体素特征
            coors: (M_total, 4)       - 体素坐标 [batch_id, z, y, x]
            num_points: (M_total,)    - 每个体素内的实际点数

        可视化（单个样本的体素化过程）:

            原始点云 (N, 3)
            每个点: (x=50.3, y=12.7, z=1.2)
                ↓
            PointToVoxel: 根据坐标分配到网格
                ↓
            体素内容 vox_: (M, 10, 3)
            ┌──────────────────────────────┐
            │ 体素#0: [(50.1,12.5,1.0),     │ ← 该体素包含3个点
            │          (50.3,12.7,1.2),    │
            │          (50.2,12.6,1.1),    │
            │          (0,0,0), ... ]      │ ← 不足10个用0填充
            │ 体素#1: [(100.5,30.2,0.8),    │ ← 只有1个点
            │          (0,0,0), ... ]      │
            │ ...                          │
            └──────────────────────────────┘
                ↓ sum(dim=1)  (voxelize_reduce=True)
            体素特征 vox_: (M, 3)
            ┌──────────────────────────────┐
            │ 体素#0: (150.6, 37.8, 3.3)    │ ← 3个点坐标求和
            │ 体素#1: (100.5, 30.2, 0.8)    │ ← 1个点直接保留
            │ ...                          │
            └──────────────────────────────┘

        注: 使用 @torch.no_grad() 因为体素化是纯几何操作，无需梯度
        """
        B, _, _ = pc.shape
        # B = batch_size (如24)

        vox_list, coors_list, num_points_list = [], [], []

        for i in range(B):
            # -------- 单样本体素化 --------
            vox_, coors_, num_points_ = self.ptvoxel(pc[i])
            # pc[i].shape = (N, 3)
            #
            # 返回:
            #   vox_:        (M_i, 10, 3) - M_i个非空体素，每个最多10个点
            #   coors_:      (M_i, 3)     - 体素网格坐标 (z_idx, y_idx, x_idx)
            #                               ← spconv默认ZYX顺序！
            #   num_points_: (M_i,)       - 每个体素实际点数 [1..10]
            #
            # 典型值: M_i ≈ 30,000~80,000 (取决于点云密度)

            if self.voxelize_reduce:
                vox_ = vox_.sum(dim=1)
                # (M_i, 10, 3) → (M_i, 3)
                #
                # 将每个体素内所有点的坐标求和
                # 效果: 密度加权的位置特征
                #   - 多点体素: 求和值大 → 隐含"这里点多"的信息
                #   - 单点体素: 保持原值
                #
                # 为什么用SUM而非MEAN?
                #   SUM同时编码了位置和密度信息
                #   网络可以通过学习来区分这两种信息

            # -------- 添加batch索引 --------
            batch_id = torch.full([vox_.shape[0], 1], i, dtype=torch.int32, device=vox_.device)
            # batch_id.shape = (M_i, 1)
            # batch_id = [[i], [i], ..., [i]]

            coors_list.append(torch.cat([batch_id, coors_], dim=1))
            # (M_i, 1) cat (M_i, 3) → (M_i, 4)
            # 每个体素坐标: [batch_id, z_idx, y_idx, x_idx]

            vox_list.append(vox_)
            num_points_list.append(num_points_)

        # -------- 合并所有batch --------
        return torch.cat(vox_list, 0), torch.cat(coors_list, 0), torch.cat(num_points_list, 0)
        # vox:        (M_total, 3)   - M_total = ΣM_i
        # coors:      (M_total, 4)   - [batch_id, z_idx, y_idx, x_idx]
        # num_points: (M_total,)
        #
        # 典型值: M_total ≈ 24 × 50,000 = 1,200,000

    def forward(self, pc):
        """
        完整前向传播: 点云 → BEV特征图

        输入:
            pc: (B, C, N) = (B, 3, N)
                B = batch_size (如24)
                C = 3 (x, y, z坐标)
                N = 点数 (如~120,000，已padding到统一长度)

        输出:
            out: (B, 128, 100, 100) - BEV特征图
                128 = 特征维度 (与图像分支一致)
                100×100 = BEV网格 (X方向100格, Y方向100格)

        完整数据流:
            (B, 3, N)
                ↓ permute
            (B, N, 3)
                ↓ voxelize
            vox (M, 3) + coors (M, 4)
                ↓ 特征/坐标重排序
            vox (M, 3) + coors (M, 4)  [xyz顺序]
                ↓ SparseEncoder
            (B, 256, 100, 100)
                ↓ ProjectionHead
            (B, 128, 100, 100)
        """
        B, C, N = pc.shape
        # B=24, C=3, N≈120,000
        # pc[b, 0, :] = 所有点的x坐标
        # pc[b, 1, :] = 所有点的y坐标
        # pc[b, 2, :] = 所有点的z坐标

        pc = pc.permute(0, 2, 1).contiguous()
        # (B, 3, N) → (B, N, 3)
        # 转换为每行一个点: [x, y, z]

        # ===================================================================
        # 步骤1: 体素化
        # ===================================================================
        vox, coors, num_points = self.voxelize(pc)
        # vox:   (M, 3) - 每个体素的坐标特征 (求和后)
        # coors: (M, 4) - [batch_id, z_idx, y_idx, x_idx] (spconv ZYX顺序)
        #
        # 数据统计示例:
        #   总点数: 24 × 120,000 = 2,880,000
        #   非空体素: M ≈ 1,200,000 (约占总格数的4.6%)
        #   压缩比: 2,880,000 → 1,200,000 (约2.4:1)

        # ===================================================================
        # 步骤2: 特征和坐标重排序
        # ===================================================================
        #
        # 目的: 将spconv的ZYX惯例转换为XYZ顺序
        #       使特征通道顺序与坐标顺序一致
        #

        # -------- 2.1 分解特征通道 --------
        z_part, y_part, x_part, c_part = vox[:, 0:1], vox[:, 1:2], vox[:, 2:3], vox[:, 3:C]
        # vox有3列(C=3)，所以:
        #   z_part = vox[:, 0:1]   (M, 1) - 第1个坐标分量
        #   y_part = vox[:, 1:2]   (M, 1) - 第2个坐标分量
        #   x_part = vox[:, 2:3]   (M, 1) - 第3个坐标分量
        #   c_part = vox[:, 3:3]   (M, 0) - 空 (没有额外特征)

        # -------- 2.2 重组特征顺序 --------
        vox = torch.cat([c_part, x_part, y_part, z_part], dim=1)
        # (M, 3) - 重新排列为 [x_part, y_part, z_part]
        # 确保特征通道与网格坐标的空间语义对齐

        # -------- 2.3 重排坐标顺序 --------
        coors = coors[:, [0, 3, 2, 1]] # zyx to xyz
        # 原始:  [batch_id, z_idx, y_idx, x_idx]
        # 重排:  [batch_id, x_idx, y_idx, z_idx]
        #
        # 为什么要重排?
        #   原始spconv返回ZYX顺序，这里统一为XYZ
        #   配合 sparse_shape = (800, 800, 41) 使用:
        #     维度0 (800): X方向 → [0, 200]m
        #     维度1 (800): Y方向 → [-100, 100]m
        #     维度2 (41):  Z方向 → [-10, 10]m
        #
        # 注: 传入SparseConvTensor后，spconv把维度0当作"Z"、
        #     维度2当作"X"来处理——但由于3D卷积各方向对称,
        #     实际效果等价。最终dense输出的空间轴顺序也随之对应。

        # ===================================================================
        # 步骤3: 稀疏3D卷积编码
        # ===================================================================
        out = self.sparse_encoder(vox, coors, B)
        #
        # 输入:
        #   features: (M, 3)   - 体素特征
        #   coors:    (M, 4)   - [batch, dim0, dim1, dim2]
        #   batch_size: B
        #
        # SparseEncoder内部流程:
        #
        # ┌─────────────────────────────────────────────────────────┐
        # │ conv_input: SubMConv3d(3→16, k=3)                      │
        # │   (M, 3) → (M, 16)                                    │
        # │   空间不变: (800, 800, 41)                              │
        # │                                                         │
        # │ Layer 0:                                                │
        # │   SparseBasicBlock(16,16) ← 残差连接                   │
        # │   SparseBasicBlock(16,16) ← 残差连接                   │
        # │   SparseConv3d(16→32, stride=2, pad=1) ← 下采样        │
        # │   空间: (800,800,41) → (400,400,21)                    │
        # │                                                         │
        # │ Layer 1:                                                │
        # │   SparseBasicBlock(32,32) × 2                          │
        # │   SparseConv3d(32→64, stride=2, pad=1) ← 下采样        │
        # │   空间: (400,400,21) → (200,200,11)                    │
        # │                                                         │
        # │ Layer 2:                                                │
        # │   SparseBasicBlock(64,64) × 2                          │
        # │   SparseConv3d(64→128, stride=2, pad=[1,1,0]) ← 下采样 │
        # │   空间: (200,200,11) → (100,100,5)                     │
        # │   注: Z方向padding=0，压缩更多                         │
        # │                                                         │
        # │ Layer 3:                                                │
        # │   SparseBasicBlock(128,128) × 2                        │
        # │   无下采样                                              │
        # │   空间: (100,100,5) → (100,100,5)                      │
        # │                                                         │
        # │ conv_out: SparseConv3d(128→128, stride=(1,1,2))        │
        # │   X,Y不变，Z再压缩一次                                 │
        # │   空间: (100,100,5) → (100,100,2)                      │
        # │                                                         │
        # │ dense(): 稀疏→稠密                                     │
        # │   → (B, 100, 100, 2, 128)                              │
        # │      ↑   ↑    ↑   ↑  ↑                                │
        # │      B   X    Y   Z  C                                 │
        # │                                                         │
        # │ view + permute:                                         │
        # │   (B, 100, 100, 2, 128) → (B, 100, 100, 256)          │
        # │   → (B, 256, 100, 100)                                 │
        # └─────────────────────────────────────────────────────────┘
        #
        # 输出: out.shape = (B, 256, 100, 100)
        #   256 = Z(2) × C(128) - 两个Z层的特征拼接
        #   100×100 = BEV网格 (X方向×Y方向)

        B, C, H, W = out.shape
        # B=24, C=256, H=100, W=100

        # ===================================================================
        # 步骤4: 投影头 - 压缩至128维
        # ===================================================================
        out = out.permute(0, 2, 3, 1).reshape(B*H*W, C)
        # (B, 256, 100, 100) → (B, 100, 100, 256) → (B×100×100, 256)
        # = (240000, 256)
        #
        # 将每个BEV格子视为独立样本，送入ProjectionHead

        out = self.proj_head(out)
        # (240000, 256) → (240000, 128)
        #
        # ProjectionHead内部:
        #   x → Linear(256, 128) → GELU → Linear(128, 128) → Dropout
        #        ↓                                              ↓
        #        └──────────── 残差连接(add) ──────────────────→ +
        #                                                        ↓
        #                                                    LayerNorm
        #                                                        ↓
        #                                                   输出 (128维)
        #
        # 意义:
        #   将两个Z层(低层+高层)的128维特征融合为一个128维向量
        #   每个BEV格子从"有两层高度信息"变成"一个综合特征"

        out = out.view(B, H, W, -1).permute(0, 3, 1, 2).contiguous()
        # (240000, 128) → (B, 100, 100, 128) → (B, 128, 100, 100)
        #
        # 恢复为标准特征图格式: (Batch, Channel, Height, Width)

        return out
        # out.shape = (B, 128, 100, 100)
        # 每个格子128维特征，覆盖100×100的BEV网格
        # 物理范围: X[0,200m], Y[-100,100m]，每格2m×2m


# ============================================================
# SparseEncoder 详细结构说明
# ============================================================
"""
稀疏卷积 vs 普通卷积:

    普通3D卷积 (Dense Conv3d):
        输入: (B, C, 800, 800, 41) → 需要 ~21GB 显存
        计算: 对所有26,240,000个格子做卷积（含大量空格子）
        ✗ 显存和计算量不可接受

    稀疏3D卷积 (Sparse Conv3d):
        输入: 只存储M个非空体素 → 约几百MB
        计算: 只在非空体素及其邻居上做卷积
        ✓ 效率提升100-1000倍

两种稀疏卷积:
    
    SubMConv3d (Submanifold Sparse Convolution):
        - 输出只在输入非空的位置非空
        - 不会"扩散"稀疏性
        - 用于残差块，保持稀疏度
        
        输入:  □ □ □ □      输出:  □ □ □ □
               □ ■ □ □             □ ■ □ □   ← 只有输入非空位置有输出
               □ □ ■ □             □ □ ■ □
               □ □ □ □             □ □ □ □
    
    SparseConv3d (Regular Sparse Convolution):
        - 输出可以在卷积核覆盖的所有位置非空
        - 会"填充"邻近区域
        - 用于下采样层，带stride=2
        
        输入:  □ □ □ □      输出(stride=2):  ■ □
               □ ■ □ □                       □ ■   ← 空间缩小一半
               □ □ ■ □                                 同时密度可能增加
               □ □ □ □

SparseBasicBlock (残差块):
    
    输入 ──→ SubMConv3d(k=3) → BN → ReLU → SubMConv3d(k=3) → BN ──→ + → ReLU → 输出
      │                                                                ↑
      └───────────────────── 残差连接 (shortcut) ──────────────────────┘
    
    特点: 不改变空间尺寸和通道数，梯度直通
"""


# ============================================================
# 空间尺寸详细推导
# ============================================================
"""
卷积输出尺寸公式:
    output = floor((input + 2×padding - kernel) / stride) + 1

Layer 0 下采样 (kernel=3, stride=2, padding=1):
    X: floor((800 + 2 - 3) / 2) + 1 = 400
    Y: floor((800 + 2 - 3) / 2) + 1 = 400
    Z: floor((41 + 2 - 3) / 2) + 1 = 21

Layer 1 下采样 (kernel=3, stride=2, padding=1):
    X: floor((400 + 2 - 3) / 2) + 1 = 200
    Y: floor((400 + 2 - 3) / 2) + 1 = 200
    Z: floor((21 + 2 - 3) / 2) + 1 = 11

Layer 2 下采样 (kernel=3, stride=2, padding=[1,1,0]):
    X: floor((200 + 2 - 3) / 2) + 1 = 100
    Y: floor((200 + 2 - 3) / 2) + 1 = 100
    Z: floor((11 + 0 - 3) / 2) + 1 = 5
    注: Z方向padding=0，使Z维度压缩更快

conv_out (kernel=3, stride=(1,1,2), padding=(1,1,0)):
    X: floor((100 + 2 - 3) / 1) + 1 = 100  ← 不变
    Y: floor((100 + 2 - 3) / 1) + 1 = 100  ← 不变
    Z: floor((5 + 0 - 3) / 2) + 1 = 2      ← 压缩到2层

最终网格: (100, 100, 2) × 128通道

通道数变化:
    输入 → conv_input → L0     → L1     → L2      → L3     → conv_out
    3   →    16      → 32     → 64     → 128     → 128    → 128
"""


